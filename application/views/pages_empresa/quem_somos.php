                                        <h2>Princípios da Atuação</h2>
                                        <p>É o código de ética da Alexcom e constituem a base da solidez e do prestígio da empresa. Os Princípios de Atuação agrupam um conjunto de diretrizes que guiam as atividades da companhia, garantindo a confiança dos clientes, profissionais, acionistas, fornecedores e sociedade. <br />
                                        Os Princípios de Atuação se aplicam aos colaboradores em todos os países de atuação e estão estruturados a partir dos seguintes princípios gerais:</p>
        				                
                                        <h4>Honestidade e confiança</h4>
                                        <p>Honramos os compromissos assumidos, demonstrando nossa integridade em todas as negociações. Protegemos a confidencialidade da informação de nossos clientes, funcionários, acionistas e fornecedores e disponibilizamos, a esses grupos de interesse, informações relevantes sobre como utilizamos e armazenamos os respectivos dados.  Caso a segurança se veja comprometida em algum momento, atuamos de forma rápida e responsável.</p>

                                        <h4>Respeito à lei​</h4>
                                        <p>Prezamos pelo cumprimento de todas as legislações, normas e obrigações regulatórias, tanto nacionais como internacionais, considerando, ainda, as políticas e normas internas. Competimos, nos nossos mercados, de forma íntegra e consideramos que os consumidores e a sociedade em geral se beneficiam com mercados abertos e livres.</p>

                                        <h4>Integridade</h4>
                                        <p>Em nenhum caso, oferecemos ou aceitamos presentes, convites ou qualquer tipo de incentivo que possa recompensar ou influenciar uma decisão empresarial. Evitamos qualquer conflito de interesses que possa beneficiar prioridades pessoais em detrimento das coletivas. Assumimos um comportamento correto, sem agir em benefício próprio ou de terceiros. Agimos institucionalmente com absoluta neutralidade política e nos abstemos de qualquer tomada de posição (direta ou indireta), a favor ou contra os processos e seus agentes políticos legítimos. Não fazemos doações em dinheiro ou espécie, de nenhum tipo, a partidos políticos, organizações, facções, movimentos, entidades, de caráter público ou privado, cuja atividade esteja claramente vinculada à atividade política.</p>

                                        <h4>Direitos humanos​</h4>
                                        <p>Respeitamos os princípios da Declaração Universal dos Direitos Humanos das Nações Unidas, bem como as declarações da Organização Internacional do Trabalho. Incentivamos a igualdade de oportunidades e tratamos todos com justiça e imparcialidade, sem preconceitos de raça, cor, nacionalidade, etnia, religião, gênero, orientação sexual, estado civil, idade, deficiência ou responsabilidade familiar.</p>
                                        
